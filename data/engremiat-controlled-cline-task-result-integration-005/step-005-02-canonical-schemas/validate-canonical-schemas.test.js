'use strict';
const fs=require("fs");
const path=require("path");
const assert=require("assert");
const validator=require(path.resolve(process.argv[2]));
const paths={execution:process.argv[3],result:process.argv[4],review:process.argv[5],evidence:process.argv[6],relations:process.argv[7]};
const output=path.resolve(process.argv[8]);
const tests=[];
function check(name,condition){tests.push({name,passed:Boolean(condition)});assert.ok(condition,name);}
const bundle=validator.validateBundle(paths);
check("bundle_valid",bundle.valid===true);
check("schema_count_four",bundle.schema_count===4);
check("relation_nodes_seven",bundle.relation_node_count===7);
check("relation_edges_seven",bundle.relation_edge_count===7);
const execution=JSON.parse(fs.readFileSync(paths.execution,"utf8").replace(/^\uFEFF/,""));
const result=JSON.parse(fs.readFileSync(paths.result,"utf8").replace(/^\uFEFF/,""));
const review=JSON.parse(fs.readFileSync(paths.review,"utf8").replace(/^\uFEFF/,""));
const evidence=JSON.parse(fs.readFileSync(paths.evidence,"utf8").replace(/^\uFEFF/,""));
const relations=JSON.parse(fs.readFileSync(paths.relations,"utf8").replace(/^\uFEFF/,""));
check("execution_immutable",execution.properties.immutable.const===true);
check("result_immutable",result.properties.immutable.const===true);
check("review_append_only",review.properties.immutable_history.const===true);
check("evidence_not_embedded",evidence.properties.content_embedded.const===false);
check("evidence_secret_free",evidence.properties.secret_values_present.const===false);
check("retry_disabled",execution.properties.automatic_retry.const===false);
check("continuation_disabled",execution.properties.automatic_continuation.const===false);
check("terminal_states_defined",execution.properties.terminal_state.enum.includes("TERMINAL_SUCCESS")&&execution.properties.terminal_state.enum.includes("TERMINAL_ERROR")&&execution.properties.terminal_state.enum.includes("CANCELLED"));
check("review_pending_defined",review.properties.review_state.enum.includes("PENDING"));
check("success_defaults_review",relations.default_success_task_status==="REVIEW");
check("terminal_success_separate_from_completion",relations.invariants.includes("TERMINAL_SUCCESS_DOES_NOT_IMPLY_TASK_COMPLETED"));
check("idempotency_defined",relations.invariants.includes("DUPLICATE_SOURCE_EXECUTION_IMPORT_IS_IDEMPOTENT"));
check("provider_metadata_isolated",relations.provider_specific_namespace==="result.provider_metadata");
check("authorization_per_execution",relations.invariants.includes("AUTHORIZATION_IS_CONSUMED_PER_EXECUTION"));
check("evidence_hash_required",evidence.required.includes("sha256"));
check("result_hash_required",result.required.includes("payload_sha256"));
const final={valid:tests.every(test=>test.passed),test_count:tests.length,passed_count:tests.filter(test=>test.passed).length,tests,cline_invoked:false,process_started:false,task_dispatched:false,prompt_submitted:false,external_network:false,git_write:false,created_at:new Date().toISOString()};
fs.writeFileSync(output,JSON.stringify(final,null,2)+"\n","utf8");
console.log(JSON.stringify({valid:final.valid,test_count:final.test_count,passed_count:final.passed_count}));
